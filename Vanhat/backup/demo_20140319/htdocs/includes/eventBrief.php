<h4>
    9.-10.5.2014, Viena 1918: Tovereita ja heimoveljiä
</h4>
<h3>
    Sipoossa
</h3>
<p>
    <i>
        Suomen vapaussota oli loppunut, maa makasi laakereillaan, Helsinki hehkui hallitusriitojaan, punikit istuivat karsinoissaan. Mutta Vienan Karjalasta päin soittivat ikäänkuin salaperäiset sotakellot yksilön korvaan: Heimoveli! Heimoveli! Missä viivyt? Kussa kuhnit? Riennä ruttoh, riennä ruttoh!... Otin vuoteeni ja kävin. (Ilmari Kianto)
    </i>
</p>
<p>
    Viena 1918: Tovereita ja heimoveljiä on historiaa elävöittävä liveroolipeli vuoden 1918 kansainvälisestä selkkauksesta Vienan Karjalassa. Tyyliltään peli edustaa Harmaasusien peleistä tuttua historiallista draamaa, ja toivomme pelaajien valmistautuvan huolella ja panostavan ajankuvan luomiseen sekä immersion ylläpitämiseen läpi pelin. Peliin otetaan 50 pelaajaa. Pelimaksu on 35€ (Harmaasudet ry:n jäsenille 30€). Pelissä on konflikteja joita mallinnetaan kevyillä, draamaan tähtäävillä säännöillä. 
</p>